defmodule CC.Editor do
  @moduledoc """
  The Editors context.
  """

  import Ecto.Query, warn: false
  alias CC.Repo

  alias CC.Editor.Sessions

  @doc """
  Gets a single session.

  Raises `Ecto.NoResultsError` if the Session does not exist.

  ## Examples

      iex> get_session!(123)
      %Session{}

      iex> get_session!(456)
      ** (Ecto.NoResultsError)

  """
  def get_session!(id), do: Repo.get!(Sessions, id)

  @doc """
  Creates a session.

  ## Examples

      iex> create_session(%{field: value})
      {:ok, %Session{}}

      iex> create_session(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_session(attrs \\ %{}) do
    %Sessions{}
    |> Sessions.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Deletes a session.

  ## Examples

      iex> delete_session(session)
      {:ok, %Session{}}

      iex> delete_session(session)
      {:error, %Ecto.Changeset{}}

  """
  def delete_session(%Sessions{} = session) do
    # TODO: also remove files
    Repo.delete(session)
  end
end
